/**
 * Aqui se filtra el Inventario segun palabras clave del Buscador por ejemplo "Router"
 * @param evt 
 * @author Sergio Iturbe Sánchez
 */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String busqueda = txtBuscar.getText().toLowerCase();
javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaInventario.getModel();
modelo.setRowCount(0);

Dao.MaterialDAO dao = new Dao.MaterialDAO();
java.util.List<Modelo.Material> materiales = dao.obtenerMateriales();

for (Modelo.Material m : materiales) {
    if (m.getNombre().toLowerCase().contains(busqueda) ||
        m.getCategoria().toLowerCase().contains(busqueda)) {
        Object[] fila = {m.getNombre(), m.getCategoria(), m.getUbicacion(), m.getCantidad(), m.getEstado()};
        modelo.addRow(fila);
    }
}
    }//GEN-LAST:event_btnBuscarActionPerformed
/**
 * Este Boton permite al Admin añadir un nuevo material al Inventario
 * Se presentan varias opciones como Descripcion-Cantidad-Estados-Su ID de categoria y Ubicacion...
 * @param evt 
 * @author Sergio Iturbe Sánchez
 */
    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        String nombre = javax.swing.JOptionPane.showInputDialog(this, "Nombre del material:");
if (nombre == null || nombre.isEmpty()) return;
String descripcion = javax.swing.JOptionPane.showInputDialog(this, "Descripción:");
String cantidadStr = javax.swing.JOptionPane.showInputDialog(this, "Cantidad:");
String estado = javax.swing.JOptionPane.showInputDialog(this, "Estado (disponible/prestado/reparacion/Baja):");
String idCatStr = javax.swing.JOptionPane.showInputDialog(this, "ID Categoría (1=Red, 2=Hardware, 3=Herramientas):");
String idUbicStr = javax.swing.JOptionPane.showInputDialog(this, "ID Ubicación (1, 2 o 3):");

try {
    int cantidad = Integer.parseInt(cantidadStr);
    int idCat = Integer.parseInt(idCatStr);
    int idUbic = Integer.parseInt(idUbicStr);
    Dao.MaterialDAO dao = new Dao.MaterialDAO();
    dao.insertar(nombre, descripcion, cantidad, estado, idCat, idUbic);
    javax.swing.JOptionPane.showMessageDialog(this, "Material añadido correctamente");
    cargarTabla();
} catch (Exception ex) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error al añadir: " + ex.getMessage());
}
    }//GEN-LAST:event_btnAñadirActionPerformed
/**
 * Este otro boton permite Elimiar un Material del Inventario, hay que selecionar una fila, si no te muestra un aviso
 * @param evt 
 * @author Sergio Iturbe Sánchez
 */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaInventario.getSelectedRow();
if (fila == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un material de la tabla");
    return;
}
int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "¿Eliminar este material?");
if (confirm == javax.swing.JOptionPane.YES_OPTION) {
    javax.swing.table.DefaultTableModel modelo = 
        (javax.swing.table.DefaultTableModel) tablaInventario.getModel();
    int id = (int) modelo.getValueAt(fila, 0);
    Dao.MaterialDAO dao = new Dao.MaterialDAO();
    dao.eliminar(id);
    javax.swing.JOptionPane.showMessageDialog(this, "Material eliminado");
    cargarTabla();
}
    }//GEN-LAST:event_btnEliminarActionPerformed
