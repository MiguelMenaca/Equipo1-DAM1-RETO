/**
 * Este boton te deja Editar un material existente en el Inventario preguntandote por las Nuevas Opciones de Modificacion
 * @param evt 
 * @author Sergio Iturbe Sánchez
 */
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tablaInventario.getSelectedRow();
if (fila == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un material de la tabla");
    return;
}
javax.swing.table.DefaultTableModel modelo = 
    (javax.swing.table.DefaultTableModel) tablaInventario.getModel();
int id = (int) modelo.getValueAt(fila, 0);
String nombreActual = (String) modelo.getValueAt(fila, 1);

String nuevoNombre = javax.swing.JOptionPane.showInputDialog(this, "Nuevo nombre:", nombreActual);
if (nuevoNombre == null) return;
String cantidadStr = javax.swing.JOptionPane.showInputDialog(this, "Nueva cantidad:", modelo.getValueAt(fila, 3));
String nuevoEstado = javax.swing.JOptionPane.showInputDialog(this, "Nuevo estado:", modelo.getValueAt(fila, 4));

try {
    int cantidad = Integer.parseInt(cantidadStr);
    Dao.MaterialDAO dao = new Dao.MaterialDAO();
    dao.actualizar(id, nuevoNombre, cantidad, nuevoEstado);
    javax.swing.JOptionPane.showMessageDialog(this, "Material actualizado");
    cargarTabla();
} catch (Exception ex) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());

    }//GEN-LAST:event_btnEditarActionPerformed
    }
