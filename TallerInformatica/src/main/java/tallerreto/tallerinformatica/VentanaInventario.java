/**
 * Aqui se obtiene de la Base de Datos y se rellena en las columnas los datos
 * @author Sergio Iturbe Sánchez
 */
private void cargarTabla() {
    String[] columnas = {"ID", "Nombre", "Categoría", "Ubicación", "Cantidad", "Estado"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);
    
    Dao.MaterialDAO dao = new Dao.MaterialDAO();
    java.util.List<Modelo.Material> materiales = dao.obtenerMateriales();
    
    for (Modelo.Material m : materiales) {
        Object[] fila = {m.getId(), m.getNombre(), m.getCategoria(), m.getUbicacion(), m.getCantidad(), m.getEstado()};
        modelo.addRow(fila);
    }
    tablaInventario.setModel(modelo);
}

